package org.albert._01_Functions

fun add(a: Float, b: Float): Float = a + b
fun sub(a: Float, b: Float): Float = a - b
fun mul(a: Float, b: Float): Float = a * b
fun div(a: Float, b: Float): Float = a / b

fun main() {
    println(add(34f, 67f))
    println(sub(34f, 67f))
    println(mul(34f, 67f))
    println(div(34f, 67f))
}
